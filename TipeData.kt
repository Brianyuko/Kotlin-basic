fun main(){

    // Tipe data diawali dengan huruf kapital
    val umur: Int = 30
    val nama: String = "Arsyi" // String, tipe data teks
    val lulus: Boolean = true // Boolean, tipe data yang hanya bernilai true atau false
    val nilaiMatematika: Float = 21.23F // Float, tipe data pecahan
    val nilaiNonAkademik: Double = 98.123123 // Double, tipe data pecahan, ukuran penyimpanan lebih besar dari float
    val nilaiFotografi: Char = 'A' // Char, tipe data karakter

    println("Nama Mahasiswa : $nama")
    println("Umur Mahasiswa : $umur")
    println("Nilai Matematika : $nilaiMatematika")
    println("Nilai Non Akademik: $nilaiFotografi atau $nilaiNonAkademik")
    print("Lulus bernilai $lulus")


}